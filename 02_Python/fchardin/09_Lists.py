#Listas 
#Es importante darles un nombre descriptivo

#Cada elemento tiene un índice. Python comienza a contar desde 0
#ïndices     0        1         2          3         4        5
amigos = ["Ross", "Monica", "Rachel", "Chandler", "Joey", "Phoebe"]
print(amigos)
print(amigos[0])
print(amigos[2])
#Se pueden usar índices negativos (empieza a contar desde el final de la lista)
print(amigos[-1])
#O varios elementos (en este caso desde el de índice 2 al final de la lista)
print(amigos[2:])
#O desde el principio exceptuando los ultimos 3 
print(amigos[:-3])